#include <stdio.h>
#include <locale.h>

int main() {
    setlocale(LC_CTYPE, "RUS");

    int a, b;
    printf("Введите два целых числа a и b: ");
    scanf("%d %d", &a, &b);

    // Верхняя граница таблицы
    printf("_______________________________________________________\n");

    // Заголовки таблицы
    printf("|      a * b      |       a+b       |       a-b       |\n");

    // Разделитель
    printf("-------------------------------------------------------\n");

    // Выражения
    printf("|  %5d * %-5d  |  %5d + %-5d  |  %5d - %-5d  |\n",
        a, b, a, b, a, b);

    // Разделитель
    printf("-------------------------------------------------------\n");

    // Результаты
    printf("|    %9d    |    %9d    |    %9d    |\n",
        a * b, a + b, a - b);

    // Нижняя граница
    printf("_______________________________________________________\n");

    return 0;
}